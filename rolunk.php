a mozdonyokról, melyekkel alapítványunk tevékenysége 
a legszorosabban összefügg.<br><br>
<ul>
<li>Egy <a href=hu05_1.html>nemzetközi történeti áttekintésben</a>
megismerhetjük a magyarországi NOHAB-ok őseit: az ausztrál Clyde 
Engineering mozdonyait és az eredeti F-sorozatot, melyet az EMD 
épített az Egyesült Államokban. A Clyde-on és a NOHAB-on kívül 
további gyártókról is olvashatunk (AFB, BruNivel, Henschel), 
melyek szintén epítettek hasonló gépeket az EMD licencével.<br><br>

<li>Az <a href=hu05_2.html>M61-es sorozat rövid története</a>
elmondja, hogyan kerülhetett egy svéd mozdony egy szocialista ország 
fővonalaira, hogyan vívott ki magának példa nélküli tiszteletet és 
rajongást, és hogyan végeztek három és fél évtized után a sorozattal.<br><br>

<li>A <a href=hu05_3.html>műszaki leírásból</a> megtudhatjuk, mit
rejt magában egy M61-es és miért különb a hazai vontatójárművek
többségénél.<br><br>

<li>A <a href=hu05_5.html>NOHAB gyár rövid története</a>
bemutatja az M61-esek egykori gyártójának fejlődését.
<br><br>

<li>Végül egy <a href=hu05_4.html>képtár</a> a NOHAB-okról és 
világszerte működő rokonaikról.<br><br>

</ul>



