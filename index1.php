<img src="../pix/010ice.jpg" width=256 height=328 hspace=12 vspace=6 border=0 align="left">
Népes külföldi rokonságukhoz hasonlóan hazánkban is igen nagy népszerűségnek
örvendenek az M61-es sorozatú mozdonyok, melyeket legtöbben &#150; gyártójuk nyomán
&#150; NOHAB néven ismernek. A gépek kimagasló megbízhatóságukkal és egyszerű
kezelhetőségükkel igen nagy megbecsülést vívtak ki maguknak a vasutasok körében
és az utazóközönség is megkedvelte őket barátságos megjelenésük és dallamos, lágy 
hangjuk miatt. Az M61-esek közel négy évtizedes pályafutásuk alatt valóságos legendává
váltak a magyar vasúton és sokak szíve fájt értük, amikor 2000 karácsonya előtt
végleg elbúcsúztak a menetrendi forgalomtól.
<br>
<br>
1999-ben a NOHAB-ok néhány elkötelezett barátja elhatározta, hogy egy civil szervezet,
a NOHAB-GM Alapítvány keretei közt tartja életben az M61-esek legendáját. Legfőbb célunk, hogy
helyreállítsuk és működőképes állapotban őrizzük meg a sorozat egyik példányát az utókor 
számára. Különvonatainkat non-profit alapon kívánjuk közlekedtetni, hogy a magyar
vasútbarátok hazai viszonyok közt is méltányos áron, a mindennapos üzemnek megfelelő
állapotában ismerhessék meg a mozdonyt. Terveink közt szerepel népszerűsítő előadások,
kiállítások szervezése és ismeretterjesztő kiadványok megjelentetése is, melyeknek a laikus 
érdeklődők és a hozzáértők egyaránt hasznát vehetik. Tevékenységünk ugyan úttörő jellegűnek
számít hazánkban, de eddigi eredményeink mellett az is bíztató, hogy Európában és világszerte
számos hasonló szervezet munkáját koronázta már siker.
<br>
<br>

Alapítványunk az <b>M61-010</b> pályaszámú mozdony megőrzését tűzte ki célul,
mivel ezt a gépet egyik alapítónk már több mint tizenöt éve segíti jelentős anyagi
áldozattal és kétkezi munkával a túlélés felé vezető úton. A tapolcai honállomás 
közbenjárása mellett ennek köszönhető, hogy a mozdony már kétszer megmenekült a lángvágótól,
sőt, 1997-ben sor kerülhetett magánkézből finanszírozott külső és részleges belső 
felújítására is.
<br>
<br>

A mozdony megvásárlását illetően alapítványunk tagjai már 1998. óta kapcsolatban
álltak a MÁV-val. Az évek során ugyan számos akadályt kellett leküzdenünk, ám
erőfeszítéseinket végül siker koronázta: 2001 novemberében megszületett a mozdony 
megvásárlásáról szóló szerződés. Mivel a mozdony eladása egy versenytárgyalás keretei
közt történt, így alapítványunknak az eredeti várakozásokhoz képest igen magas
árat, 8&nbsp;125&nbsp;000&nbsp;Ft-ot kell fizetnie a mozdonyért. Ez többszörösen
meghaladja azt az összeget, amennyiért hozzánk hasonló nyugat-európai vasútbarát
társaságok szerezték be megőrizni kívánt járműveiket.
<br>
<br>

Alapítványunk immár kifizette a vételárat és a mozdony időközben új telephelyére, a Keleti
pályaudvar egykori fűtőházába került. További jelentős próbatételeknek nézünk azonban elébe;
hátra van ugyanis a mozdony üzemképessé tétele, restaurálása, melyhez további támogatókat,
szponzorokat keresünk. A mozdony már a felújítási munkák során ingyenesen megtekinthető,
ehhez csupán a látogatás időpontjának előzetes egyeztetése szükséges. A későbbiekben
lehetőséget kívánunk nyújtani arra is, hogy az érdeklődők szaktudásuknak megfelelően
maguk is kivegyék részüket a helyreállítási munkákból. A helyreállítás tervezett menetéről, az eddig elvégzett 
műveletekről <a href="hu00_1.html">itt</a> található bővebb információ.
<br>
<br>

Alapítványunkat 2000 januárjában kiemelten közhasznú társadalmi szervezetként jegyezte
be a bíróság. Az adományozó cégek &#150; a megfelelő feltételek mellett &#150; az
alapítvány számára juttatott adományok 150%-át írhatják le az adóalapból, magánszemélyek
pedig személyi <a href="hu_001.html#1p">jövedelemadójuk 1%-át</a> is felajánlhatják.
Alapítványunk elsősorban a magánszemélyek, érdeklődő vasútbarátok számára egy többlépcsős
<a href="hu_001.html">tagsági rendszert</a> dolgozott ki, melynek &#150; különösen hazai
érdeklődőkre gondolva &#150; egy kedvezményes &#132;diák&#148; kategória is része. Bármilyen 
egyéb támogatást is szívesen fogadunk, és a segítséget igyekezzük méltó módon viszonozni. A
tagsági díjat fizető magánszemélyek &#150; az ingyenes ajándékokon túl &#150; jelentős
kedvezményeket kapnak különvonatainkon, a támogató cégek pedig megegyezés alapján lehetőséget
kapnak kiadványainkban, rendezvényeinken történő bemutatkozásra. 
<br>
<br>

Tevékenységünkről készséggel adunk <a href="hu_002.html">további tájékoztatást</a>. 
Előre is köszönjük az érdeklődést és támogatást!
